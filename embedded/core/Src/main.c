char* arrayToString(uint16_t* array, size_t length) {
    // Estimate the maximum length of the resulting string
    // Each uint16_t can be up to 5 characters long (max value 65535), plus one for the space or null terminator
    size_t maxStringLength = length * 6;
    char* result = (char*)malloc(maxStringLength);
    if (result == NULL) {
        perror("Failed to allocate memory");
        exit(EXIT_FAILURE);
    }

    char* current = result;
    for (size_t i = 0; i < length; ++i) {
        // Write the current number and a space to the string, updating the pointer
        current += sprintf(current, "%hu ", array[i]);
    }

    // Replace the last space with a null terminator if the array was not empty
    if (length > 0) {
        *(current - 1) = '\0';
    } else {
        *current = '\0'; // Ensure the string is null-terminated even if the array is empty
    }

    return result;
}

